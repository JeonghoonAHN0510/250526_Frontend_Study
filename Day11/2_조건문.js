// 문제 1: 합격/불합격 판정
// 두 개의 점수를 입력받아 총점을 계산하세요. 총점이 90점 이상이면 '성공'을, 그렇지 않으면 '실패'를 출력하는 프로그램을 작성하시오.
// (1) 두 개의 점수를 입력받아
let score1 = Number(prompt("[문제1]점수를 입력하세요."));
let score2 = Number(prompt("[문제1]점수를 입력하세요."));
// (2) 총점을 계산하세요.
let total = score1 + score2;

if ( total >= 90 ) {            // (3) 만약에(if) 총점(total)이 90 이상(>=)이면
    console.log("성공");        // (4) true "성공"을 출력
} else {                        // (5) 그렇지 않으면
    console.log("실패");        // (6) false "실패"를 출력
}

